/* C++ Program to demonstrate use of right
   shift operator */
//https://www.geeksforgeeks.org/left-shift-right-shift-operators-c-cpp/
#include <stdio.h>
int main()
{
	unsigned char a = 6, b = 7, c = 8, d = 9, e = 10;

	printf("%d>>1 = %d\n", a, a >> 1);
	printf("%d>>1 = %d\n", b, b >> 1);
	printf("%d>>1 = %d\n", c, c >> 1);
	printf("%d>>1 = %d\n", d, d >> 1);
	printf("%d>>1 = %d\n", e, e >> 1);

	return 0;
}
